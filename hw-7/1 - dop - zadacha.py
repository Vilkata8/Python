#KeyError - Poqvqva se kogato se opitame da dostupim element, koito ne e definiran v programata.
#AttributeError - Poqvqva se kogato prepratka kum atribut ili prisvoqvane e neuspeshno.
#ValueError - Poqvava se kogato potrebitel podade nevalidna stoinost na funkciq, no e s validen argument.
#NameError - Poqvqva se kogato se opitame da dostupim greshno vuvedena danna ili takava, kakvato ne sme definirali.
#TypeError - Poqvqva se kogato se opitame da izvurshim operaciq varhu nepravilen tip obekt.
#IndexError - Poqvqva se kogato se opitame da dostupim element ot spisuk ili kortej, koito ne prisiustva v tqh.
#ModuleNotFoundError - Poqvqva se kogato se opitame da dostupim modul, koito ne moje da bude nameren.
#ZeroDivisionError - Poqvqva se kogato se opitame da razdelim element na 0.
#RunTimeError - Poqvqva se po vreme na izpulnenreto na koda.
#FileNotFoundError - Poqvqva se kogato programatata ne uspee da nameri zadadeniq fail v koda.
#OSEroor - Poqvqva se kogato se poqvi problem s opredelena funkciq na operacionnata sistema.